CREATE SCHEMA IF NOT EXISTS car_shop_schema;

CREATE TABLE IF NOT EXISTS car_shop_schema.cars (
        id INT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
        brand VARCHAR(100),
        model VARCHAR(100),
        year VARCHAR(4),
        price TEXT,
        condition TEXT
        );

